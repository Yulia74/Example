void PrintImage(int[,] image)     // метод, чтобы вместо нулей выводился пробел
{
    for (int i = 0; i < image.GetLength(0); i++)
    {
        for (int j = 0; j < image.GetLength(1); j++)
        {
            if (image[i, j] == 0) Console.Write($" ");
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}

void FillImage(int row, int col)   // метод, который будет закрашивать картинку
{                                  // в качестве аргумента - точка, с которой 
    if (pic[row, col] == 0)         // начнется раскрашивание
    {
        pic[row, col] = 1;
        FillImage(row - 1, col);      // метод вызывает сам себя, определяем правило движения закрашивания (вверх)
        FillImage(row, col - 1);      // влево
        FillImage(row + 1, col);      // вниз
        FillImage(row, col + 1);      // вправо
    }
}




